class Cars {
  final String id;
  final String nome;
  final String marca;
  final String modelo;
  final int ano;
  final String placa;
  final String cor;
  final String chassi;
  final DateTime compra;
  final int valor;

  const Cars({
    required this.id,
    required this.nome,
    required this.marca,
    required this.modelo,
    required this.ano,
    required this.placa,
    required this.cor,
    required this.chassi,
    required this.compra,
    required this.valor,
  });
}
